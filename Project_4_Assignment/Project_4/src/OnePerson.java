import java.util.*;

public class OnePerson{
    private int id;
    private int gender;
    private int time;

    public int getID(){
        return id;
    }

    public int getGender(){
        return gender;
    }

    public int getTime(){
        return time;
    }

    public void setID(int id){
        this.id = id;
    }

    public void setGender(int gender){
        this.gender = gender;
    }

    public void setTime(int time){
        this.time = time;
    }

}
